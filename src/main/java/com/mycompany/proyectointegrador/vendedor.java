    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        // TODO add your handling code here:
        Number value = (Number) precioPeli.getValue();
        double precio = value.doubleValue();

        // Redondear a dos decimales usando BigDecimal
        BigDecimal bd = new BigDecimal(precio).setScale(2, RoundingMode.HALF_UP);
        precio = bd.doubleValue();
        
        if(precio<=0){
            JOptionPane.showMessageDialog(null, "Precio Inválido");
            return;
        }
        
        DefaultTableModel table = (DefaultTableModel) tablaInventario.getModel();
        
        //Guardar en inventario
        Producto aux= new Producto();
        aux.setNombre(nombrePeli.getText());
        aux.setPrecio((double)Math.floor(precio*100)/100);
        aux.setCantidad((int)cantidadPeli.getValue());
        inventario.add(aux);
        
        //Añadir a la tabla
        table.addRow(new Object[]{aux.getNombre(),aux.getPrecio(),aux.getCantidad()});
        
        //Limpiar campos
        nombrePeli.setText("");
        precioPeli.setText("");
        cantidadPeli.setValue(1);
    }//GEN-LAST:event_agregarActionPerformed

