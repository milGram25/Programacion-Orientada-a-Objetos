    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        // TODO add your handling code here:
        inventario.remove(buscarProducto(eliminarLabel.getText(),inventario));
        DefaultTableModel modelo = (DefaultTableModel)tablaInventario.getModel();
        for(int i=0;i<modelo.getRowCount();i++){
            if(modelo.getValueAt(i, 0).equals(eliminarLabel.getText())){
                modelo.removeRow(i);
                
                //Reiniciar campos y terminar ciclo
                eliminarField.setText("");
                eliminarLabel.setText("---------------------------------");
                eliminarDatos.setText("-------------------------------");
                eliminar.setEnabled(false);
                return;
            }
        }
    }//GEN-LAST:event_eliminarActionPerformed

    private void busquedaModificarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_busquedaModificarKeyReleased
        // TODO add your handling code here:
        DefaultListModel lista = new DefaultListModel();
        if(busquedaModificar.getText().equals("")){
            listaPelis.setModel(lista);
            return;
        }
        
        for(int i=0;i<inventario.size();i++){
            if(inventario.get(i).getNombre().contains(busquedaModificar.getText())){
                lista.addElement(inventario.get(i).getNombre());
            }
        }
        listaPelis.setModel(lista);
    }//GEN-LAST:event_busquedaModificarKeyReleased

    private void listaPelisMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaPelisMouseClicked
        // TODO add your handling code here:

        try{
        selected=inventario.get(buscarProducto(listaPelis.getSelectedValue(),inventario));
        } catch (IndexOutOfBoundsException e){
            return;
        }
        
        //Ingresar datos del producto
        nombreModi.setText(selected.getNombre());
        precioModi.setValue(selected.getPrecio());
        cantModi.setValue(selected.getCantidad());
        
        //Activar campos y botones
        nombreModi.setEnabled(true);
        precioModi.setEnabled(true);
        cantModi.setEnabled(true);
        modificar.setEnabled(true);
    }//GEN-LAST:event_listaPelisMouseClicked

    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
        // TODO add your handling code here:
        int index=buscarProducto(selected.getNombre(),inventario);
        Number value = (Number) precioModi.getValue();
        double precio = value.doubleValue();

        // Redondear a dos decimales usando BigDecimal
        BigDecimal bd = new BigDecimal(precio).setScale(2, RoundingMode.HALF_UP);
        
        //Modificar Tabla
        DefaultTableModel inv=(DefaultTableModel)tablaInventario.getModel();
        for(int i=0;i<inv.getRowCount();i++){
            System.out.println("Entro veces "+(i+1));
            if(inv.getValueAt(i, 0).equals(inventario.get(index).getNombre())){
                System.out.println("Si lo encontro");
                inv.setValueAt(nombreModi.getText(), i, 0);
                inv.setValueAt(bd.doubleValue(), i, 1);
                inv.setValueAt(cantModi.getValue(), i, 2);
                break;
            }
        }
        
        //Guardar en inventario
        inventario.get(index).setNombre(nombreModi.getText());
        inventario.get(index).setCantidad((int)cantModi.getValue());
        
        inventario.get(index).setPrecio(bd.doubleValue());
        
        System.out.println(inventario.get(index));
        
        nombreModi.setText("");
        cantModi.setValue(1);
        precioModi.setValue(0);
        
        //Inicializar los componentes
        nombreModi.setEnabled(false);
        precioModi.setEnabled(false);
        cantModi.setEnabled(false);
        modificar.setEnabled(true);
        listaPelis.clearSelection();
        busquedaModificar.setText("");
        listaPelis.removeAll();
    }//GEN-LAST:event_modificarActionPerformed

